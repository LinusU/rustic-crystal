impl PokemonSpecies {
    pub fn name(self) -> PokeString {
        const START: usize = (0x14 * 0x4000) | (0x7384 & 0x3fff);
        let offset = START + (u8::from(self) as usize - 1) * 10;
        PokeString::from_bytes(&ROM[offset..], 10)
    }
}

